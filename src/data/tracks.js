function buildTracks(themeId) {
  return trackNames[themeId].map(([name, subtitle], index) => ({
    id: `${themeId}-${String(index + 1).padStart(2, '0')}`,
    themeId,
    name,
    subtitle,
    style: styles[index],
    root: roots[themeId][index],
    scale: scales[themeId][index],
    chords: chordSets[themeId][index],
    noteInterval: noteIntervals[index],
    chordDuration: 14 + index * 1.5,
  }))
}

export const tracksByTheme = {
  forest: buildTracks('forest'),
  rain: buildTracks('rain'),
  ocean: buildTracks('ocean'),
  night: buildTracks('night'),
}

export function getTracksForTheme(themeId) {
  return tracksByTheme[themeId] ?? []
}

export function getTrackById(trackId) {
  for (const list of Object.values(tracksByTheme)) {
    const found = list.find((track) => track.id === trackId)
    if (found) return found
  }
  return null
}
